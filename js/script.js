let currentMode = 'login';
let currentLang = 'en';
let currentTheme = 'dark';
let currentUser = null;

function readStorage(key, fallback) {
  try {
    const stored = localStorage.getItem(key);
    return stored ? JSON.parse(stored) : fallback;
  } catch (error) {
    return fallback;
  }
}

function writeStorage(key, value) {
  localStorage.setItem(key, JSON.stringify(value));
}

function getUsers() {
  return readStorage('habitify_users', {});
}

function saveUsers(users) {
  writeStorage('habitify_users', users);
}

function saveSession(email) {
  writeStorage('habitify_session', { email });
}

function clearSession() {
  localStorage.removeItem('habitify_session');
}

function setTheme(theme) {
  currentTheme = theme;
  document.body.dataset.theme = theme;
  elements.themeSwitch.checked = theme === 'light';
  elements.themeIcon.textContent = theme === 'light' ? '☀️' : '🌙';
  localStorage.setItem('habitify_theme', theme);
}

function setLanguage(lang) {
  currentLang = lang;
  elements.languageSelector.value = lang;
  localStorage.setItem('habitify_lang', lang);
  applyTranslations();
}

function translate(key) {
  return translations[currentLang][key] || translations.en[key] || key;
}

