function showTab(mode) {
  currentMode = mode;
  elements.loginTab.classList.toggle('active', mode === 'login');
  elements.signupTab.classList.toggle('active', mode === 'signup');
  elements.nameGroup.hidden = mode !== 'signup';
  applyTranslations();
}

function showScreen(auth) {
  elements.authScreen.hidden = !auth;
  elements.dashboardScreen.hidden = auth;
}

function getSession() {
  return readStorage('habitify_session', null);
}

function buildUser(email) {
  return {
    name: 'Habit Hero',
    email,
    password: '',
    coins: 0,
    habits: [],
    streak: 0,
    premium: false,
    createdAt: Date.now(),
  };
}

function loadUserFromSession() {
  const session = getSession();
  if (!session || !session.email) {
    return null;
  }
  const users = getUsers();
  const user = users[session.email];
  return user || null;
}

function restoreApp() {
  const savedTheme = localStorage.getItem('habitify_theme') || 'dark';
  const savedLang = localStorage.getItem('habitify_lang') || 'en';
  setTheme(savedTheme);
  setLanguage(savedLang);
  const sessionUser = loadUserFromSession();
  if (sessionUser) {
    currentUser = sessionUser;
    showScreen(false);
    renderDashboard();
  } else {
    showScreen(true);
  }
}

function showToast(message) {
  elements.toast.textContent = message;
  elements.toast.classList.add('show');
  elements.toast.classList.remove('hidden');
  clearTimeout(window.toastTimeout);
  window.toastTimeout = setTimeout(() => {
    elements.toast.classList.remove('show');
    window.toastTimeout = setTimeout(() => elements.toast.classList.add('hidden'), 250);
  }, 2200);
}

function renderDashboard() {
  if (!currentUser) return;
  elements.welcomeName.textContent = currentUser.name || translations[currentLang].welcomeNameDefault;
  elements.coinCounter.textContent = currentUser.coins;
  elements.habitCount.textContent = currentUser.habits.length;
  elements.streakCount.textContent = currentUser.streak || 0;
  const progress = Math.min(100, (currentUser.streak || 0) * 10);
  elements.progressFill.style.width = `${progress}%`;
  populateHabits();
  if (currentUser.premium) {
    elements.buyPremium.textContent = '✓ Premium';
    elements.buyPremium.disabled = true;
    elements.buyPremium.classList.add('btn-secondary');
  } else {
    elements.buyPremium.disabled = false;
    elements.buyPremium.classList.remove('btn-secondary');
    elements.buyPremium.textContent = translations[currentLang].buyPremium;
  }
}

function populateHabits() {
  elements.habitList.innerHTML = '';
  if (!currentUser) return;
  const today = new Date().toISOString().slice(0, 10);
  currentUser.habits.forEach((habit) => {
    const item = document.createElement('li');
    item.className = 'habit-item';
    item.innerHTML = `
      <div>
        <h4>${habit.title}</h4>
        <p>${habit.goal || ''}</p>
      </div>
      <div class="habit-actions">
        <button class="btn btn-secondary" data-action="complete" data-id="${habit.id}">${habit.lastCompleted === today ? 'Completed' : 'Complete'}</button>
        <button class="btn btn-outline" data-action="delete" data-id="${habit.id}">✕</button>
      </div>
    `;
    elements.habitList.appendChild(item);
  });
}

function saveCurrentUser() {
  const users = getUsers();
  users[currentUser.email] = currentUser;
  saveUsers(users);
}

function handleAuthSubmit(event) {
  event.preventDefault();
  const name = elements.fullName.value.trim();
  const email = elements.emailInput.value.trim().toLowerCase();
  const password = elements.passwordInput.value;
  if (currentMode === 'signup') {
    if (!name || !email || !password) {
      showToast(translations[currentLang].toastSignupEmpty);
      return;
    }
    const users = getUsers();
    if (users[email]) {
      showToast(translations[currentLang].toastUserExists);
      return;
    }
    const newUser = buildUser(email);
    newUser.name = name;
    newUser.password = password;
    newUser.coins = 300;
    users[email] = newUser;
    saveUsers(users);
    saveSession(email);
    currentUser = newUser;
    showScreen(false);
    renderDashboard();
    showToast(translations[currentLang].toastSignedUp);
    elements.authForm.reset();
  } else {
    if (!email || !password) {
      showToast(translations[currentLang].toastLoginEmpty);
      return;
    }
    const users = getUsers();
    const found = users[email];
    if (!found || found.password !== password) {
      showToast(translations[currentLang].toastLoginFailed);
      return;
    }
    currentUser = found;
    saveSession(email);
    showScreen(false);
    renderDashboard();
    showToast(translations[currentLang].toastSignedIn);
    elements.authForm.reset();
  }
}

function handleGoogleLogin() {
  const email = prompt('Please enter your Google email', 'googleuser@example.com');
  if (!email || !email.includes('@')) return;
  const normalized = email.trim().toLowerCase();
  const users = getUsers();
  let user = users[normalized];
  if (!user) {
    user = buildUser(normalized);
    user.name = normalized.split('@')[0];
    user.password = '';
    user.coins = 500;
    users[normalized] = user;
    saveUsers(users);
  }
  currentUser = user;
  saveSession(normalized);
  showScreen(false);
  renderDashboard();
  showToast(translations[currentLang].toastSignedIn);
}

function addHabit(event) {
  event.preventDefault();
  const title = elements.habitTitle.value.trim();
  const goal = elements.habitGoal.value.trim();
  if (!title) return;
  const newHabit = {
    id: `habit-${Date.now()}`,
    title,
    goal,
    lastCompleted: '',
    streak: 0,
  };
  currentUser.habits.unshift(newHabit);
  saveCurrentUser();
  renderDashboard();
  showToast(translations[currentLang].toastAddedHabit);
  elements.habitForm.reset();
}

function completeHabit(id) {
  const habit = currentUser.habits.find((item) => item.id === id);
  if (!habit) return;
  const today = new Date().toISOString().slice(0, 10);
  if (habit.lastCompleted === today) {
    showToast(translations[currentLang].toastAlreadyCompleted);
    return;
  }
  const yesterday = new Date(Date.now() - 86400000).toISOString().slice(0, 10);
  if (habit.lastCompleted === yesterday) {
    habit.streak += 1;
  } else {
    habit.streak = 1;
  }
  habit.lastCompleted = today;
  currentUser.coins += 50;
  currentUser.streak = Math.max(currentUser.streak, habit.streak);
  saveCurrentUser();
  renderDashboard();
  showToast(translations[currentLang].toastCompleted);
}

function deleteHabit(id) {
  currentUser.habits = currentUser.habits.filter((item) => item.id !== id);
  saveCurrentUser();
  renderDashboard();
}

function handleHabitListClick(event) {
  const button = event.target.closest('button');
  if (!button) return;
  const action = button.dataset.action;
  const id = button.dataset.id;
  if (!action || !id) return;
  if (action === 'complete') {
    completeHabit(id);
  }
  if (action === 'delete') {
    deleteHabit(id);
  }
}

function buyPremium() {
  if (currentUser.premium) return;
  if (currentUser.coins >= 2000) {
    currentUser.coins -= 2000;
    currentUser.premium = true;
    saveCurrentUser();
    renderDashboard();
    showToast(translations[currentLang].toastPremiumBought);
  } else {
    showToast(translations[currentLang].toastInsufficientCoins);
  }
}

function logout() {
  clearSession();
  currentUser = null;
  showScreen(true);
  showTab('login');
  showToast(translations[currentLang].toastLoggedOut);
}

function initEvents() {
  elements.themeSwitch.addEventListener('change', () => {
    setTheme(elements.themeSwitch.checked ? 'light' : 'dark');
  });
  elements.languageSelector.addEventListener('change', (event) => {
    setLanguage(event.target.value);
  });
  elements.loginTab.addEventListener('click', () => showTab('login'));
  elements.signupTab.addEventListener('click', () => showTab('signup'));
  elements.authForm.addEventListener('submit', handleAuthSubmit);
  elements.googleLogin.addEventListener('click', handleGoogleLogin);
  elements.habitForm.addEventListener('submit', addHabit);
  elements.habitList.addEventListener('click', handleHabitListClick);
  elements.buyPremium.addEventListener('click', buyPremium);
  elements.logoutBtn.addEventListener('click', logout);
}

document.addEventListener('DOMContentLoaded', () => {
  initEvents();
  restoreApp();
});
